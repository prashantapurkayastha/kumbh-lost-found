const TRANSLATIONS: Record<string, Translations> = { en, mr, hi, gu, bn, te, ta, pa };

export function t(key: TKey, lang: string): string {
  const dict = TRANSLATIONS[lang] ?? TRANSLATIONS.hi;
  return dict[key] ?? TRANSLATIONS.en[key];
}
